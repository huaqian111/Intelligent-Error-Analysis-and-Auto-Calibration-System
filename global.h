#ifndef __GLOBAL_H
#define __GLOBAL_H

#include "./SYSTEM/sys/sys.h"

/*==============================================================================
 * 风扇控制参数结构体
 *============================================================================*/
typedef struct {
    uint8_t manual_enable;   /* 0:自动模式, 1:强制手动模式 */
    uint8_t speed;           /* 风扇转速 0~100 */
    uint8_t auto_active;     /* 自动状态指示（温度超标标志） */
} FanControl_t;

/*==============================================================================
 * 传感器数据结构体
 *============================================================================*/
typedef struct {
    float   ds18b20_temp;      /* DS18B20 温度 (℃) */
    uint8_t ds18b20_online;    /* DS18B20 在线标志 */
    float   pt100_temp;        /* PT100 温度 (℃) */
    uint8_t pt100_fault;       /* PT100 故障标志 */
    float   vref_voltage;      /* MCU 参考电压 (V) */
    float   internal_temp;     /* MCU 内部温度 (℃) */

    /* 光环境传感器 */
    uint16_t als;              /* 环境光强度 */
    uint16_t ps;               /* 接近感应 */
    uint16_t ir;               /* 红外强度 */

    /* DHT11 温湿度 */
    float   dht11_temp;        /* DHT11 温度 (℃) */
    uint8_t dht11_humi;        /* DHT11 湿度 (%) */

    /* 风扇 */
    FanControl_t fan;
} SensorData_t;

/*==============================================================================
 * 诊断结果数据结构体
 *============================================================================*/
typedef struct {
    uint8_t health_score;      /* 健康分数 0~100 */
    uint32_t error_code;       /* 错误代码 (0:正常, 1:警告, 2:故障) */
    char advice_str[64];       /* 建议字符串 */
} DiagResult_t;

/*==============================================================================
 * 全局宏定义（可根据工程调整）
 *============================================================================*/
/* DHT11 读取间隔（单位：100ms） */
#define DHT11_READ_INTERVAL     5      /* 10 * 100ms = 1秒 */

/* DS18B20 读取间隔（单位：100ms） */
#define DS18B20_READ_INTERVAL   5       /* 3 * 100ms ≈ 300ms */

/* 自动温控阈值 */
#define TEMP_HIGH               28.5f   /* 超过此温度开启风扇 */
#define TEMP_LOW                27.5f   /* 低于此温度关闭风扇 */

/* 风扇最大转速限制（实际PWM占空比 = fan.speed / 2，映射到50%） */
#define FAN_SPEED_MAX           100     /* 网页/触摸屏显示最大值，减半输出 */

#endif /* __GLOBAL_H */